// If-else condition
let elixir = 4;

if (elixir >= 5) {
  console.log("Expensive card");
} else {
  console.log("Playable card");
}

// Loop example
for (let i = 1; i <= 3; i++) {
  console.log("Battle " + i);
}

// While loop
let count = 0;
while (count < 2) {
  console.log("Charge!");
  count++;
}
