// ðŸ”¹ Declare a variable using let (can change later)
let name = "Royale";
console.log(typeof name); // "string"

// ðŸ”¹ Use const for values that won't change
const elixirCost = 4;
console.log(typeof elixirCost); // "number"

// ðŸ”¹ Boolean type
let isLegendary = true;
console.log(typeof isLegendary); // "boolean"

// ðŸ”¹ Null vs undefined
let result = null;
let notSet;
console.log(typeof result);  // "object" (weird JS behavior)
console.log(typeof notSet);  // "undefined"
